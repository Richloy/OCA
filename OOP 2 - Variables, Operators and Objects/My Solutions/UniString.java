class UniString {
	public static void main(String[] args) {
		char euro = '\u20AC';
		char one = '1';
		char eight = '8';
		System.out.println(euro +""+ one +""+ eight +""+ eight);

		char j = 'j';
		char o = 'o';
		char e = 'e';
		System.out.println(Character.toUpperCase(j)+""+Character.toUpperCase(o) +""+Character.toUpperCase(e));
	}
}
